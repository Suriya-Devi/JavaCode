package assignments;
//Assignement 25-Even & odd numbers from 0 to 100

public class Ass_25_Even_and_OddNumbers {

	public static void main(String[] args) {
		for(int i=0;i<=100;i++) {
			if(i%2==0) {
				System.out.println("Even numnber "+i);
			}
			else {
				System.out.println("Odd number "+i);
			}
		}

	}

}
